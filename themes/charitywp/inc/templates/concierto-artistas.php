							<h2>Fausto Palma</h2>
							<p>Fausto Palma es un compositor y multi-instrumentalista mexicano, nacido en 1976.
								Su vida está marcada por la movilidad. El periplo inició en 1993, cuando tomó sus primeras lecciones en la guitarra.
							</p>
							<p>	
								Desde entonces, ya sea en La Habana, El Cairo, Nueva Delhi, el Distrito Federal, Londres o Ámsterdam, se ha encontrado con el sarangi, el oud y el cabaquinho, instrumentos a los cuales ha tratado de robar sus misterios.
							</p>
							<p>	
								En 1999, crea Asallam, un grupo con influencias pastorales y sinfónicas, sonidos místicos y misteriosos de Oriente. Su nomadismo lo llevó a Europa, Siria, Egipto, Jordania y Líbano. De su paso por Nueva Delhi, además de su contacto con el sarangi, queda Ghazali (Cero Records, 2004). De regreso en México, el guitarrista se dio a la creación de Petra, grupo que toma su nombre de la ciudad jordana.
							</p>
							<p>	
								El más reciente capítulo de esta historia en construcción es Dawn (Intolerancia, 2009), álbum doble en el que a las influencias ya citadas hay que añadir sicodelia, krautrock, rock progresivo, ritmos antillanos y africanos.
							</p>
							<p>	
								Es el guitarrista de Songs for Eleonor de Andrés Almeida
							</p>
							<p>	
								Entre los años 2010 y 2017 a grabado los discos: Lágrima, Módulos, Primavera Defeña, Strictly Personal.
							</p>
